"""
Problem 8 - Largest Product in a Series
https://projecteuler.net/problem=8
"""


def largest_product_in_series(s):
    width = 13
    size = len(s)
    current = 0
    largest_product = 0
    while current + width < size:
        substr = s[current:current + width]
        prod = 1
        for c in substr:
            prod = prod * int(c)
        if prod > largest_product:
            largest_product = prod
        current = current + 1
    return largest_product


